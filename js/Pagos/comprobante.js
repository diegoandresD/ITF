function Fetch(Ruta, Data) {
    let _Ruta = Ruta;
    let _Datos = Data;

    const FetchWithData = () => {
        return new Promise(Resolve => {
            return Resolve(
                fetch(_Ruta, {
                    method: 'POST',
                    body: JSON.stringify(_Datos),
                    headers: {
                        'Content-Type': 'application/json'
                    }
                }).then(res => res.json())
                    .then(response => response)
            );
        });
    };

    const FetchWithOutData = () => {
        return new Promise(Resolve => {
            return Resolve(
                fetch(_Ruta, {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json'
                    }
                }).then(res => res.json())
                    .then(response => response)
            );
        });
    };

    return {
        FetchWithData: FetchWithData,
        FetchWithOutData: FetchWithOutData
    };
}

window.addEventListener('load', async () => {

    let array = localStorage.getItem('BUY_ORDEN');

    let url = "../Pagos/DetalleComprobante";
    let data = { cod: Number(array) };
    const Fetchs = Fetch(url, data);
    const Resultado = await Fetchs.FetchWithData();
    if (Resultado.RESPUESTA) {
        let comprobante = Resultado.DATA;
        document.getElementById('CARD_NUMBER').innerText = "************" + comprobante.NUMERO_TARJETA;
        document.getElementById('BUY_ORDEN').innerText = comprobante.ORDEN_COMPRA;
        document.getElementById('AUTHORIZATION_CODE').innerText = comprobante.CODIGO_AUTORIZACION;
        document.getElementById('PAYMENT_TYPE_CODE').innerText = comprobante.MEDIO_PAGO;
        document.getElementById('TRANSACTION_DATE').innerText = moment(comprobante.FECHA).format("DD/MM/YYYY HH:MM:SS");
        document.getElementById('TOTAL').innerText = "$" + addCommas(comprobante.TOTAL);
    }
    localStorage.removeItem('_cart');

});

function addCommas(nStr) {
    nStr += '';
    var x = nStr.split('.');
    var x1 = x[0];
    var x2 = x.length > 1 ? '.' + x[1] : '';
    var rgx = /(\d+)(\d{3})/;
    while (rgx.test(x1)) {
        x1 = x1.replace(rgx, '$1' + '.' + '$2');
    }
    return x1 + x2;
}